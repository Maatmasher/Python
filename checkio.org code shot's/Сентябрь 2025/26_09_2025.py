'''код реализует простой алгоритм отсечения выбросов, удаляя заданное количество наименьших и наибольших значений из отсортированного набора данных.'''

def checkio(data: set[int], total:int) -> set[int]:
    
    if not total:
        return data

    order = sorted(data)
    order_sl = order[total:-total]

    return set(order_sl)

