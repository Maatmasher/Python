"""код решает задачу поиска ближайшего значения, корректно обрабатывая случаи, когда несколько чисел находятся на одинаковом расстоянии от целевого значения (выбирает наименьшее из них)."""


def checkio(values: set, one: int) -> int:

    return min(values, key=lambda k: (abs(k - one), k))


print(checkio({1, 2, 3, 4, 5}, 3))  # 3
print(checkio({5, 10, 8, 12, 89}, 7))  # 8
print(checkio({1, 3, 5}, 4))  # 3 (а не 5!)
print(checkio({10, 20}, 15))  # 10 (при равном расстоянии выбирает меньшее)
